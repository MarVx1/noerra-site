_DEFAULT_SCENARIO = "discovery"

# Ротация в памяти процесса — чтобы не повторять одну и ту же аналогию
# подряд для одной темы. Без персистентности в БД (сознательный
# компромисс, см. план доработки): при перезапуске бота ротация
# сбрасывается, но схема database/db.py остаётся нетронутой.
_last_used: dict[str, str] = {}


def build_analogy(topic: str, scenario: str) -> str:
    """Возвращает аналогию для темы/сценария; не повторяет последнюю подряд."""
    pool = TOPIC_ANALOGIES.get(topic) or GENERIC_ANALOGIES.get(scenario) or GENERIC_ANALOGIES[_DEFAULT_SCENARIO]

    key = topic if topic in TOPIC_ANALOGIES else f"scenario:{scenario}"
    last = _last_used.get(key)
    candidates = [a for a in pool if a != last] or pool

    choice = random.choice(candidates)
    _last_used[key] = choice
    return choice
