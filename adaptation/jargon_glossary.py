# ============================================================
#  adaptation/jargon_glossary.py — расшифровка методологического
#  жаргона поведенческой нейронауки (стандартные тесты на грызунах),
#  который иначе попадает в текст переведённым дословно и непонятным
#  ("тест на предпочтение сахарозы", "приподнятый крестообразный
#  лабиринт") — см. разбор реальной публикации по теме "стресс"
#  (article id=483, статья "MeA Tac2-Nk3R signaling").
#
#  Термины и их русская форма калиброваны по фактическому переводу
#  реальных абстрактов из noerra.db (_translate — Google Translate,
#  формулировки от него не всегда одинаковые), а не выдуманы заранее.
#  Список растёт по мере появления новых терминов в реальных данных —
#  не претендует на покрытие всех методик поведенческой нейронауки.
# ============================================================

import re

# (regex по переведённому русскому тексту, короткая человеческая расшифровка)
# Расшифровка — короткая приписка в скобках, не полная замена термина:
# статья остаётся точной (название методики не теряется), но у читателя
# сразу есть смысл, не нужно гуглить. Полная замена термина проверялась
# на реальном примере (article id=483, 6 терминов в одном предложении) и
# читалась хуже — предложение теряло связность из-за грамматики списка.
_GLOSSARY: list[tuple[re.Pattern, str]] = [
    (
        re.compile(r"тест[а-я]*\s+на\s+предпочтение\s+сахарозы", re.IGNORECASE),
        "проверяет, тянется ли животное к простому удовольствию",
    ),
    (
        re.compile(r"тест[а-я]*\s+принудительного\s+плавания", re.IGNORECASE),
        "смотрит, как быстро животное «сдаётся» в стрессовой ситуации",
    ),
    (
        re.compile(r"тест[а-я]*\s+социального\s+взаимодействия", re.IGNORECASE),
        "смотрит, насколько животное тянется к сородичам",
    ),
    (
        re.compile(r"приподнят[а-я]+\s+крестообразн[а-я]+\s+лабиринт[а-я]*", re.IGNORECASE),
        "измеряет тревожность по тому, избегает ли животное открытых участков",
    ),
    (
        re.compile(r"тест[а-я]*\s+с\s+подавлением\s+новизны\s+при\s+кормлении", re.IGNORECASE),
        "измеряет тревожность в новой обстановке",
    ),
    (
        re.compile(r"тест[а-я]*\s+открытого\s+поля", re.IGNORECASE),
        "измеряет тревожность и активность в новом пространстве",
    ),
    (
        re.compile(r"(?:водн[а-я]+\s+)?лабиринт[а-я]*\s+морриса", re.IGNORECASE),
        "проверяет пространственную память и обучение",
    ),
    (
        re.compile(r"обусловленн[а-я]+\s+предпочтени[а-я]+\s+мест[а-я]*", re.IGNORECASE),
        "проверяет, тянется ли животное туда, где раньше получало вознаграждение",
    ),
]


# Кап на число аннотаций в одном тексте: на реальном примере (article
# id=483 — 6 методологических терминов подряд в одном предложении)
# аннотирование всех подряд превращало предложение в нечитаемую стену
# скобок и не помогало, а мешало — после 2-3 объяснений читатель уже
# понимает "это тесты на грызунах на такое-то поведение", дальше термины
# можно просто перечислить без расшифровки.
_MAX_ANNOTATIONS = 3


def simplify_methodology_terms(text: str, max_annotations: int = _MAX_ANNOTATIONS) -> str:
    """Добавляет короткую расшифровку в скобках при первых упоминаниях
    методологических терминов, не больше max_annotations на весь текст.
    Термины сверх лимита остаются как есть — не превращает список тестов
    в стену скобок (см. _MAX_ANNOTATIONS)."""
    if not text:
        return text
    remaining = max_annotations
    for pattern, explanation in _GLOSSARY:
        if remaining <= 0:
            break
        if pattern.search(text):
            text = pattern.sub(lambda m: f"{m.group(0)} ({explanation})", text, count=1)
            remaining -= 1
    return text
